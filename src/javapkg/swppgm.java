package javapkg;

public class swppgm {

	public static void main(String[] args) {
	int a=20,b=10,c;
	System.out.println("before swapping value of a="+a);
	System.out.println("value of b="+b);
	
    c=a;
    a=b;
    b=c;
    System.out.println("after swapping value of a="+a);
    System.out.println("value of b="+b);
    
	}

}
